/** CME equity index futures — US holiday schedule (aligned with NYSE closures). */
const CME = NYSE;

export const EXCHANGE_HOLIDAYS: Record<string, Set<string>> = {
  nyse: NYSE,
  lse: LSE,
  tse: TSE,
  asx: ASX,
  cme: CME,
};

export function isExchangeHoliday(exchangeId: string, dateKey: string): boolean {
  return EXCHANGE_HOLIDAYS[exchangeId]?.has(dateKey) ?? false;
}
